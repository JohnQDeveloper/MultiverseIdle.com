<h3>Welcome to the 2026 Alpha Edition of MultiverseIdle!</h3>

This is a work in progress, and we appreciate your feedback as we continue to develop the game.

As you are in the tutorial, the idle game elements (i.e. performing your job when you are over the energy cap) are not present.
This is meant to familiarize yourself with the basic game mechanics so you know if you want to register.
Nothing is saved unless you register.
<BR /><BR />
<h3>For now, you should perform the following tutorial tasks to familiarize yourself with our features:</h3>
<ul>
    <li><b>Go to the Gym and train the stats you want to improve until you deplete your Energy.</b>
        <ul>
            <li>Intelligence - Increases skill proc rate and crafting outcomes.</li>
            <li>Strength - Increases attack damage.</li>
            <li>Dexterity - Increases accuracy and critical strike chance.</li>
            <li>Agility - Increases evasion and speed.</li>
            <li>Constitution - Increased Life, Toxicity Resistance, and Life Recovery</li>
        </ul>
    </li>
    <li><b>Decide how you want to spend your Nerve by Delving.</b>
        <ul>
            <li>Delve into the depths of the multiverse to uncover new resources and challenges.</li>
            <li>Mining will leverage your strength with a low risk of combat encounters.</li>
            <li>Solving riddles will leverage your intelligence but a risk of serious injury</li>
            <li>Disarming traps will leverage your dexterity and agility but at a risk of injury or toxicity.</li>
            <li>Hunting monsters makes full use of your stats and skills but you might find yourself in the hospital.</li>
            <li>There are pvp objectives as well but those are locked during the tutorial as you are in a sandbox mode.</li>
        </ul>
    </li>
    <li><b>Experiment with different strategies and find what works best for you before starting the full game.</b></li>
    <li>During the tutorial, you are blocked from much of the game as it requires player interaction.</li>
    <li>Please register only once. While we allow cross trading in Perpetual, we do not allow multiple accounts.</li>
    <li>If caught, all accounts are banned and you have to start over. We don't ban permanently as I'm testing automated moderation tools.</li>

</ul>
